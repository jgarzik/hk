///
/// This is a standard PC BIOS font, commonly used in VGA text mode.
/// 256 characters * 16 bytes per character = 4096 bytes total.
pub static DEFAULT_FONT: Font = Font {
    width: 8,
    height: 16,
    data: &FONT_8X16_DATA,
};

