//! Bitmap Font for Console Rendering
//!
//! Provides an 8x16 VGA-compatible bitmap font for text console rendering.
//! Each character is represented as 16 bytes (one byte per row, 8 pixels wide).

/// Font descriptor
pub struct Font {
    /// Width of each glyph in pixels
    pub width: u8,
    /// Height of each glyph in pixels
    pub height: u8,
    /// Raw glyph data (256 glyphs * height bytes)
    pub data: &'static [u8],
}

impl Font {
    /// Get the glyph bitmap for a character
    ///
    /// Returns a slice of `height` bytes, where each byte represents one row
    /// of the glyph (MSB = leftmost pixel).
    pub fn glyph(&self, ch: u8) -> &[u8] {
        let offset = (ch as usize) * (self.height as usize);
        let end = offset + (self.height as usize);
        if end <= self.data.len() {
            &self.data[offset..end]
        } else {
            // Return empty glyph if out of bounds
            &self.data[0..self.height as usize]
        }
    }
}

